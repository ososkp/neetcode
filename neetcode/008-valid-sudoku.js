class Solution {
    /**
     * @param {character[][]} board
     * @return {boolean}
     */
    isValidSudoku(board) {
        const rowSets = Array.from({ length: 9 }, () => new Set())
        const colSets = Array.from({ length: 9 }, () => new Set())
        const squareSets = Array.from({ length: 9 }, () => new Set())
        for (let i = 0; i < 9; i++) {
            for (let j = 0; j < 9; j++) {
                const val = board[i][j]
                if (board[i][j] === '.') continue

                const si = Math.floor(i / 3) * 3
                const sj = Math.floor(j / 3)
                const box = si + sj

                if (rowSets[i].has(val)
                    || colSets[j].has(val)
                    || squareSets[box].has(val)) {
                    return false
                }

                rowSets[i].add(val)
                colSets[j].add(val)
                squareSets[box].add(val)
            }
        }
        return true
    }
}

